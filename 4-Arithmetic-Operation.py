##Operasi Aritmatika Standar
x = 5
y = 6
#Operasi Penjumlahan (+)
hasil =x+y
print(x, "+", y, "=", hasil )
#Operasi Pengurangan (-)
hasil =x-y
print(x, "-", y, "=", hasil )
#Operasi Perkalian (*)
hasil =x*y
print(x, "x", y, "=", hasil )
#Operasi Pembagian (/)
hasil =x/y
print(x, ":", y, "=", hasil )
#Operasi Eksponen (**)
# hasil =x**y
# print(x, "-", y, "=", hasil ))
#Operasi Modulus (%)
hasil =x%y
print(x, "%", y, "=", hasil )
#Operasi floor division (//)
hasil =x//y
print(x, "//", y, "=", hasil )

#Operational Precedence
"""1.()
2.Eksponen
3.Perkalian, Pembagian, Modulus, Floor division
4.Penjumblahan dan pengurangan """

